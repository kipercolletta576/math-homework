public class MathHomework {
    public static void main(String[] args) {
        // Example code for simple arithmetic operations: addition, subtraction, multiplication, division
        int num1 = 5;
        int num2 = 3;
        int sum = num1 + num2; // Sum of num1 and num2
        int difference = num1 - num2; // Difference between num1 and num2
        int product = num1 * num2; // Product of num1 and num2
        int quotient = num1 / num2; // Quotient of num1 and num2

        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + difference);
        System.out.println("Product: " + product);
        System.out.println("Quotient: " + quotient);

        if (num1 > 0) {
            System.out.println("Num1 is positive.");
        }
    }
}
